package Modelo;

/**
 *
 * @author kygon
 */
public class Candidato {

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the ciudad_origen
     */
    public String getCiudad_origen() {
        return ciudad_origen;
    }

    /**
     * @param ciudad_origen the ciudad_origen to set
     */
    public void setCiudad_origen(String ciudad_origen) {
        this.ciudad_origen = ciudad_origen;
    }

    /**
     * @return the correo
     */
    public String getCorreo() {
        return correo;
    }

    /**
     * @param correo the correo to set
     */
    public void setCorreo(String correo) {
        this.correo = correo;
    }

    /**
     * @return the descripcion
     */
    public String getDescripcion() {
        return descripcion;
    }

    /**
     * @param descripcion the descripcion to set
     */
    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    /**
     * @return the direccion
     */
    public String getDireccion() {
        return direccion;
    }

    /**
     * @param direccion the direccion to set
     */
    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    /**
     * @return the mensaje_campania
     */
    public String getMensaje_campania() {
        return mensaje_campania;
    }

    /**
     * @param mensaje_campania the mensaje_campania to set
     */
    public void setMensaje_campania(String mensaje_campania) {
        this.mensaje_campania = mensaje_campania;
    }

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the numero_documento
     */
    public String getDocumento() {
        return documento;
    }

    /**
     * @param documento the documento to set
     */
    public void setDocumento(String documento) {
        this.documento = documento;
    }

    /**
     * @return the partido_politico
     */
    public String getPartido_politico() {
        return partido_politico;
    }

    /**
     * @param partido_politico the partido_politico to set
     */
    public void setPartido_politico(String partido_politico) {
        this.partido_politico = partido_politico;
    }

    /**
     * @return the propuestas
     */
    public String getPropuestas() {
        return propuestas;
    }

    /**
     * @param propuestas the propuestas to set
     */
    public void setPropuestas(String propuestas) {
        this.propuestas = propuestas;
    }

    /**
     * @return the telefono
     */
    public String getTelefono() {
        return telefono;
    }

    /**
     * @param telefono the telefono to set
     */
    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }
    private int id;
    private String ciudad_origen;
    private String correo;
    private String descripcion;
    private String direccion;
    private String mensaje_campania;
    private String nombre;
    private String documento;
    private String partido_politico;
    private String propuestas;
    private String telefono;
}
